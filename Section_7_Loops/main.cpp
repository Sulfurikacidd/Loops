/*
 While loop: use while loop when you don't know the number of iterations. While a condition is True the loop keeps on running.
 Skeleton:
 while(condition) {}
 
 Do while loop: Kind of a while loop but it checks the condition after executing the body of the loop -- always and always runs atleast one
 Skeleton:
 do {process} while (condition);
 
 For loop: use it when you know the number of iterations.
 Skeleton:
 for(counter;condition;updation) {process}
 Steps:
 1. Counter 2. Condition 3. Process 4. Updation 5. Condition 6. Process 7. Updation 8. Check (if false exit)
 */

